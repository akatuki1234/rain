function move1(){
  let Rm =  parseInt((Math.random() * 4000) + 1); 
  const rain = document.getElementById('rain1');
    if(Rm >= 0 && Rm < 2000){
    rain.style.left = Rm+"px";
    }
    else if(Rm >= 2000 && Rm < 4000){
      rain.style.right = Rm+"px";
    }
}


